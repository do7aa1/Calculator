#include "operations.h"
#include<iostream>
double operations :: add (double a, double b)
	{
	return a + b;
	}
double operations :: sub(double a, double b)
{
	return a - b;
}
double operations :: divide(double a, double b)
{
	return a / b;
}
double operations ::  multiply(double a, double b)
{
	return a * b;
}